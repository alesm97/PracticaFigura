using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace PracticaFigura
{
    class Rectangle:Figure
    {

        public override float Perimetre(float baseSide, float heightSide)
        {
            return base.Perimetre(baseSide,heightSide);
        }

        public override float Area(float baseSide, float height)
        {
            return base.Area(baseSide, height);
        }


    }
}
